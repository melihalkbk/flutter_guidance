import 'package:flutter/material.dart';

class AnimatedSwitcherView extends StatefulWidget {
  const AnimatedSwitcherView({super.key});

  @override
  _AnimatedSwitcherViewState createState() => _AnimatedSwitcherViewState();
}

class _AnimatedSwitcherViewState extends State<AnimatedSwitcherView> {
  int _counter = 0;

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  void _decrementCounter() {
    setState(() {
      _counter--;
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text("AnimatedSwitcher in Flutter")),
      body: SingleChildScrollView(
        child: Padding(
          padding: const EdgeInsets.all(16.0),
          child: Column(
